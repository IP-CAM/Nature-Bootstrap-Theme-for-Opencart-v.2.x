	public function validate() {
		if ((utf8_strlen(trim($this->request->post['firstname'])) < 1) || (utf8_strlen(trim($this->request->post['firstname'])) > 32)) {
			$this->error['firstname'] = $this->language->get('error_firstname');
		}

		if ((utf8_strlen(trim($this->request->post['fathername'])) < 1) || (utf8_strlen(trim($this->request->post['fathername'])) > 32)) {
			$this->error['fathername'] = $this->language->get('error_fathername');
		}

		$b1 = $this->request->post['birth_date'];
		if (!is_numeric(strtotime($b1))) {
			$this->error['birth_date'] = $this->language->get('error_birth_date');
		}
		elseif (time() - strtotime($b1) < 31536000*18)
		{
			$this->error['birth_date'] = "Вам должно быть больше 18 лет";
		}

		if ((utf8_strlen(trim($this->request->post['lastname'])) < 1) || (utf8_strlen(trim($this->request->post['lastname'])) > 32)) {
			$this->error['lastname'] = $this->language->get('error_lastname');
		}

		if ((utf8_strlen($this->request->post['email']) > 96) || !preg_match('/^[^\@]+@.*.[a-z]{2,15}$/i', $this->request->post['email'])) {
			$this->error['email'] = $this->language->get('error_email');
		}
		if (!isset($this->request->post['postcode']) or $this->request->post['postcode'] == '' ) {
			//c_fix_1
			//$this->error['postcode'] = "Введите почтовый индекс";
			//!c_fix_1
		}
		/* fix_10 */
		if ($this->config->get('config_google_captcha_status')) {
			$recaptcha = file_get_contents('https://www.google.com/recaptcha/api/siteverify?secret=' . urlencode($this->config->get('config_google_captcha_secret')) . '&response=' . $this->request->post['g-recaptcha-response'] . '&remoteip=' . $this->request->server['REMOTE_ADDR']);

			$recaptcha = json_decode($recaptcha, true);

			if (!$recaptcha['success']) {
				$this->error['captcha'] = $this->language->get('error_captcha');
			}
		}
		/* fix_10 */

		if ($this->model_account_customer->getTotalCustomersByEmail($this->request->post['email'])) {
			$this->error['warning'] = $this->language->get('error_exists');
		}

		if ((utf8_strlen($this->request->post['telephone']) < 3) || (utf8_strlen($this->request->post['telephone']) > 32)) {
			$this->error['telephone'] = $this->language->get('error_telephone');
		}
		//c_fix_1
		//if ((utf8_strlen(trim($this->request->post['address_1'])) < 3) || (utf8_strlen(trim($this->request->post['address_1'])) > 128)) {
		//	$this->error['address_1'] = $this->language->get('error_address_1');
		//}
		//!c_fix_1




		$this->load->model('localisation/country');

		$country_info = $this->model_localisation_country->getCountry($this->request->post['country_id']);

		if ($country_info && $country_info['postcode_required'] && (utf8_strlen(trim($this->request->post['postcode'])) < 2 || utf8_strlen(trim($this->request->post['postcode'])) > 10)) {
			$this->error['postcode'] = $this->language->get('error_postcode');
		}

		if ($this->request->post['country_id'] == '') {
			//c_fix_1
			
			//$this->error['country'] = $this->language->get('error_country');
			
			//!c_fix_1
		}
		else
		{
			if ($this->request->post['country_id'] == 220) //Украина
			{
				
				if (!isset($this->request->post['city5']) or $this->request->post['city5'] == '') {
					//c_fix_1
					//$this->error['city'] = "Пожалуйста, введите населенный пункт";
					//!c_fix_1
				}
				
			}
			else
				if (!isset($this->request->post['city']) or $this->request->post['city'] == '' or $this->request->post['city'] == '0') {
					//c_fix_1
					//$this->error['city'] = $this->language->get('error_city');
					//!c_fix_1
				}
				
		}

		if (!isset($this->request->post['zone_id']) || $this->request->post['zone_id'] == '') {
			//c_fix_1
			//$this->error['zone'] = $this->language->get('error_zone');
			//!c_fix_1
		}
		//$this->error['agree'] = "Вы должны согласиться с политикой";
		if (!isset($this->request->post['agree']) or $this->request->post['agree'] != 1) {
			$this->error['agree'] = "Вы должны согласиться с политикой";
		}

		// Customer Group
		if (isset($this->request->post['customer_group_id']) && is_array($this->config->get('config_customer_group_display')) && in_array($this->request->post['customer_group_id'], $this->config->get('config_customer_group_display'))) {
			$customer_group_id = $this->request->post['customer_group_id'];
		} else {
			$customer_group_id = $this->config->get('config_customer_group_id');
		}

		// Custom field validation
		$this->load->model('account/custom_field');

		$custom_fields = $this->model_account_custom_field->getCustomFields($customer_group_id);

		foreach ($custom_fields as $custom_field) {
			if ($custom_field['required'] && empty($this->request->post['custom_field'][$custom_field['location']][$custom_field['custom_field_id']])) {
				$this->error['custom_field'][$custom_field['custom_field_id']] = sprintf($this->language->get('error_custom_field'), $custom_field['name']);
			}
		}

		if ((utf8_strlen($this->request->post['password']) < 4) || (utf8_strlen($this->request->post['password']) > 20)) {
			$this->error['password'] = $this->language->get('error_password');
		}

		if ($this->request->post['confirm'] != $this->request->post['password']) {
			$this->error['confirm'] = $this->language->get('error_confirm');
		}

		// Agree to terms
		if ($this->config->get('config_account_id')) {
			$this->load->model('catalog/information');

			$information_info = $this->model_catalog_information->getInformation($this->config->get('config_account_id'));

			if ($information_info && !isset($this->request->post['agree'])) {
				$this->error['warning'] = sprintf($this->language->get('error_agree'), $information_info['title']);
			}
		}
		
		return !$this->error;
	}

